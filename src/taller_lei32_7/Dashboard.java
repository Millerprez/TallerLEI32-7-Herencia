    public void imprimirIntentos(){
        String msj = "Cantidad de intentos \n";
        for (int i = 0; i < 5; i++) {
            msj += nombres[i]+" con "+intentos[i]+ " Intentos\n"; 
        }
        
        JOptionPane.showMessageDialog(null, msj);
    }
    
    public void guardarPlantasFlores(){
        String msj = "";
        for (int i = 0; i < 5; i++) {
            String dato = (String) matriz[i][1];
            if (dato.equalsIgnoreCase("Con flor")) {
                plantasConFlor[i] = (String) matriz[i][0];
            } 
        }
        
        for (int i = 0; i < 5; i++) {
            if (plantasConFlor[i]!= null) {
                msj += plantasConFlor[i]+" | ";
            }  
        }
        JOptionPane.showMessageDialog(null, "PLANTAS CON FLOR \n" + msj);
    }
    
    public void getPorcentajePlantasSombra(){
        int cantidadPlantasSombra = 0;
        for (int i = 0; i < 5; i++) {
            String dato = (String) matriz[i][3];
            if (dato.equalsIgnoreCase("Sombra")) {
                cantidadPlantasSombra++;
            } 
        }
        if (cantidadPlantasSombra == 0) {
           JOptionPane.showMessageDialog(null, "No hay plantas con ambiente sombra " ); 
        } else {
           JOptionPane.showMessageDialog(null, "PORCENTAJE PLANTAS CON AMBIENTE SOMBRA"
                   + "\n" + (cantidadPlantasSombra * 100 / 5) + "% ");
        } 
    }
    
    public void getTresDatosIguales(){
        int cantidadDatosIguales = 0;
        for (int i = 0; i < 5; i++) {
            String ambiente = (String) matriz[i][3];
            String clasificacion = (String) matriz[i][1];
            String tipo = (String) matriz[i][2];
            if (ambiente.equalsIgnoreCase("Sombra") && tipo.equalsIgnoreCase("pteridofitas")
                    && clasificacion.equalsIgnoreCase("Sin flor")) {
                cantidadDatosIguales++;
            } 
        }
        if (cantidadDatosIguales == 0) {
           JOptionPane.showMessageDialog(null, "No hay plantas con los 3 datos iguales " ); 
        } else {
           JOptionPane.showMessageDialog(null, "Hay "+ cantidadDatosIguales + " plantas de tipo pteridofitas, sin flor y con ambiente de sombra ");
        }
        
        
    }
    
