    public boolean verificarNombre(String nombre){
        for (int i = 0; i < 5; i++) {
            if (nombre.equalsIgnoreCase(nombres[i])){
                intentos[i] += 1;
                return true;
            }
        }
        
        return false;
    }  
    
    public boolean verificarTodo(String name, String type,String clase,
            String ambiente, String uno, String dos ){
        return "Seleccione".equalsIgnoreCase(name) || 
                "Seleccione".equalsIgnoreCase(clase) ||
                "Seleccione".equalsIgnoreCase(type) ||
                "Seleccione".equalsIgnoreCase(ambiente) ||
                "Seleccione".equalsIgnoreCase(uno) ||
                "Seleccione".equalsIgnoreCase(dos);
    }
    
