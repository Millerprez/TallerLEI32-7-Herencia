    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(registros != 5){
            String name = cbName.getSelectedItem().toString();
            String type = cbType.getSelectedItem().toString();
            String clase  = cbClafication.getSelectedItem().toString();
            String ambiente  = cbEnviroment.getSelectedItem().toString();
            String uno="", dos="";
            
            if(cbClafication.getSelectedItem().toString().equals("Sin flor")){
                uno = cbColorEsporas.getSelectedItem().toString();
                dos  = cbTipoAgua.getSelectedItem().toString();
            } else if(cbClafication.getSelectedItem().toString().equals("Con flor")){
                uno  = cbColorPrincipal.getSelectedItem().toString();
                dos = cbCantidadColores.getSelectedItem().toString();
            }

            if (verificarTodo(name, type, clase, ambiente, uno, dos)) {
                JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
            } else {
                if (verificarNombre(name)) {
                    JOptionPane.showMessageDialog(null, "El nombre ya se encuentra almacenado");
                } else {
                   
                    nombres[registros] = name;
                    tipos[registros] = type;
                    matriz[registros][0] = name;
                    matriz[registros][1] = clase;
                    matriz[registros][2] = type;
                    matriz[registros][3] = ambiente;

                    registros++;
                    JOptionPane.showMessageDialog(null, "Almacenado con éxito");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Al parecer ya guardaste 5 plantas, no es posible guardar más");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

